#basic list
numbers = [4, 5, 2, 10, 7]
print(numbers)

#append list
numbers = [4, 5, 2, 10, 7]
numbers.append(13) #append method added to list a 'number' or what ever u want to list
print(numbers)

#insert list
numbers = [4, 5, 2, 10, 7]
numbers.insert(2, 13) #insert method added to list what ever u want in 'index' u want
print(numbers)

#remove list
numbers = [4, 5, 2, 10, 7]
numbers.remove(5) #remove method u can delete what u want
print(numbers)

#pop list
numbers = [4, 5, 2, 10, 7]
numbers.pop() #delete last symbol in list
print(numbers)

#index list
numbers = [4, 5, 2, 10, 7]
print(numbers.index(5)) #localisate a index of '5' number